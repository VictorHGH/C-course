// Tipos de datos

#include <iostream>
using namespace std;

int main() {
  // Enteros
  int numero1 = 10;
  cout << "El valor es: " << numero1 << endl;

  // Flotantes
  float numero2 = 10.5;
  cout << "El valor es: " << numero2 << endl;

  // Dobles
  double numero3 = 12321.23434540543;
  cout << "El valor es: " << numero3 << endl;

  // Logicos
  bool misterio = true;
  cout << "El valor es: " << misterio << endl;

  // Caracteres
  char letra = '&';
  cout << "El valor es: " << letra << endl;

  // Cadenas
  string cadena = "Hola que tal";
  cout << "El valor es: " << cadena << endl;

  return 0;
}
